/**
 * Supabase policy package: filtering rules for Supabase-managed PostgreSQL
 * databases (target-architecture §3.9, stage-08-policy).
 *
 * Port of packages/pg-delta/src/core/integrations/supabase.ts into DSL v2.
 * v2 semantics: rules evaluated in order, first match wins, no match → include.
 * Rule ordering: specific INCLUDE rules first, then EXCLUDE rules.
 *
 * OLD RULE → NEW RULE MAPPING (for audit completeness)
 *
 * Old-1: objectType extension + operation create
 *        → { kind:"extension", verb:"add" } → include
 *
 * Old-2: objectType extension + operation drop
 *        → { kind:"extension", verb:"remove" } → include
 *
 * Old-3: objectType trigger AND trigger-schema IN SYSTEM
 *        AND NOT trigger-function_schema IN SYSTEM
 *        AND NOT (schema=pgmq AND table matches q_/a_ prefix)
 *        → { kind:"trigger", schema IN SYSTEM, not edgeTo{kind:"procedure", schema IN SYSTEM},
 *            not (schema=pgmq AND table glob q_* or a_*) } → include
 *
 * Old-4: "star-slash-schema" IN SUPABASE_SYSTEM_SCHEMAS
 *        → { schema: SUPABASE_SYSTEM_SCHEMAS } → exclude
 *
 * Old-5: "schema/name" IN SUPABASE_SYSTEM_SCHEMAS
 *        → { kind:"schema", name: SUPABASE_SYSTEM_SCHEMAS } → exclude
 *
 * Old-6: "star-slash-owner" IN SUPABASE_SYSTEM_ROLES
 *        → { owner: SUPABASE_SYSTEM_ROLES } → exclude
 *
 * Old-7: "role/name" IN SUPABASE_SYSTEM_ROLES
 *        → { kind:"role", name: SUPABASE_SYSTEM_ROLES } → exclude
 *
 * Old-8: membership where member OR role is a system role
 *        → { kind:"membership", idField{field:"member"} IN SYSTEM_ROLES } → exclude
 *           { kind:"membership", idField{field:"role"} IN SYSTEM_ROLES } → exclude
 *
 * Old-9: objectType foreign_data_wrapper + scope privilege
 *        → { kind:"acl", target:{kind:"fdw"} } → exclude
 *        (GRANT/REVOKE on FDW requires superuser; non-superuser postgres
 *        cannot replay this ACL regardless of FDW ownership)
 *
 * Old-10: implicitly filtered in old engine via parent-object exclusion
 *         → { kind:["acl","comment","securityLabel"], target:{schema IN SYSTEM} } → exclude
 *            { kind:["acl","comment","securityLabel"], target:{kind:"schema", name IN SYSTEM_SCHEMAS} } → exclude
 *         Two sub-rules under the unified target predicate: target.schema covers
 *         qualified-kind targets (table, view, sequence...) while target.kind+name
 *         covers simple-kind targets like schema itself (in v2, satellite deltas
 *         are independent; must be excluded explicitly)
 *
 * Old-11: emptyCatalog: undefined (TODO in old engine)
 *         → baseline: "supabase-baseline"
 *         (snapshot generated by scripts/generate-supabase-baseline.ts)
 *
 * Old-12: Wasm FDW filtering via wasm_fdw_handler/validator function names
 *         → OBSOLETE in new engine: extraction-time pg_depend 'e' rows
 *         exclude extension-member FDWs, servers, foreign tables, user
 *         mappings at extract time. The precise Wasm-name match is not
 *         ported; if extension-member objects slip through, the owner
 *         predicate or ownedByExtension predicate covers them.
 *
 * Old-13: serialize skipAuthorization for schema CREATE owned by system role
 *         → serialize rule: { kind: schema, owner: SYSTEM_ROLES } sets
 *         skipAuthorization — the planner applies serialize params PER
 *         MATCHED FACT (first-match-wins), so user schemas keep their
 *         AUTHORIZATION clause
 *
 * Old-14: serialize skipSchema for extensions installing their own schema
 *         → REMOVED. The SCHEMA clause is now derived from the extension's
 *         `relocatable` fact (pg_extension.extrelocatable): a non-relocatable
 *         extension emits a bare CREATE EXTENSION. No name list, not
 *         Supabase-specific. See docs/architecture/managed-view-architecture.md (move 2).
 *
 * BASELINE
 * The baseline field names the snapshot that represents "empty" on a Supabase
 * platform instance. Facts present-and-identical in the baseline are subtracted
 * before diffing (via subtractBaseline), replacing the old emptyCatalog TODO.
 * Regenerate: bun run scripts/generate-supabase-baseline.ts <db-url> <pg-major>
 */

